-- Move master_reason_of_delay from order_assembly_dispatch to orders (now an
-- order-level field captured at creation). Idempotent: copies any existing
-- values across, then drops the old column if it still exists.
ALTER TABLE orders ADD COLUMN IF NOT EXISTS master_reason_of_delay TEXT;
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
         WHERE table_name = 'order_assembly_dispatch'
           AND column_name = 'master_reason_of_delay'
    ) THEN
        UPDATE orders o
           SET master_reason_of_delay = ad.master_reason_of_delay
          FROM order_assembly_dispatch ad
         WHERE ad.order_id = o.id
           AND ad.master_reason_of_delay IS NOT NULL
           AND o.master_reason_of_delay IS NULL;
        ALTER TABLE order_assembly_dispatch DROP COLUMN master_reason_of_delay;
    END IF;
END $$;

-- Move pump serial onto the order itself (one PUMP S.NO. per order) and drop
-- the former order_pumps child table. Idempotent.
ALTER TABLE orders ADD COLUMN IF NOT EXISTS pump_sno TEXT;
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.tables
         WHERE table_name = 'order_pumps'
    ) THEN
        UPDATE orders o
           SET pump_sno = p.pump_sno
          FROM (
            SELECT DISTINCT ON (order_id) order_id, pump_sno
              FROM order_pumps
             ORDER BY order_id, created_at
          ) p
         WHERE p.order_id = o.id
           AND o.pump_sno IS NULL
           AND p.pump_sno IS NOT NULL;
        DROP TABLE order_pumps;
    END IF;
END $$;

-- Dispatch target dates move to the order (filled by Central Visibility after
-- payment confirmation); Planning sees them read-only. Add an order-level LD
-- flag that gates the revised date. Idempotent.
ALTER TABLE orders ADD COLUMN IF NOT EXISTS ld TEXT;
ALTER TABLE orders ADD COLUMN IF NOT EXISTS dispatch_target_date DATE;
ALTER TABLE orders ADD COLUMN IF NOT EXISTS dispatch_target_revised_date DATE;
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
         WHERE table_name = 'order_planning'
           AND column_name = 'dispatch_target_date'
    ) THEN
        UPDATE orders o
           SET dispatch_target_date =
                 COALESCE(o.dispatch_target_date, pl.dispatch_target_date),
               dispatch_target_revised_date =
                 COALESCE(o.dispatch_target_revised_date, pl.dispatch_target_revised_date)
          FROM order_planning pl
         WHERE pl.order_id = o.id;
        ALTER TABLE order_planning DROP COLUMN dispatch_target_date;
        ALTER TABLE order_planning DROP COLUMN dispatch_target_revised_date;
    END IF;
END $$;

-- LD moves from orders into order_planning (filled by Central Visibility,
-- visible to Planning). Idempotent.
ALTER TABLE order_planning ADD COLUMN IF NOT EXISTS ld TEXT;
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
         WHERE table_name = 'orders' AND column_name = 'ld'
    ) THEN
        INSERT INTO order_planning (order_id)
            SELECT o.id FROM orders o
             WHERE o.ld IS NOT NULL
               AND NOT EXISTS (SELECT 1 FROM order_planning pl WHERE pl.order_id = o.id);
        UPDATE order_planning pl
           SET ld = o.ld
          FROM orders o
         WHERE o.id = pl.order_id AND o.ld IS NOT NULL AND pl.ld IS NULL;
        ALTER TABLE orders DROP COLUMN ld;
    END IF;
END $$;

-- Actual pump status / assembled qty / assembly date move from Assembly &
-- Dispatch into Planning (filled by Planning). Idempotent.
ALTER TABLE order_planning ADD COLUMN IF NOT EXISTS actual_pump_status TEXT;
ALTER TABLE order_planning ADD COLUMN IF NOT EXISTS assembled_packed_qty TEXT;
ALTER TABLE order_planning ADD COLUMN IF NOT EXISTS assembly_date DATE;
DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
         WHERE table_name = 'order_assembly_dispatch'
           AND column_name = 'actual_pump_status'
    ) THEN
        INSERT INTO order_planning (order_id)
            SELECT ad.order_id FROM order_assembly_dispatch ad
             WHERE (ad.actual_pump_status IS NOT NULL
                    OR ad.assembled_packed_qty IS NOT NULL
                    OR ad.assembly_date IS NOT NULL)
               AND NOT EXISTS (SELECT 1 FROM order_planning pl WHERE pl.order_id = ad.order_id);
        UPDATE order_planning pl
           SET actual_pump_status = COALESCE(pl.actual_pump_status, ad.actual_pump_status),
               assembled_packed_qty = COALESCE(pl.assembled_packed_qty, ad.assembled_packed_qty),
               assembly_date = COALESCE(pl.assembly_date, ad.assembly_date)
          FROM order_assembly_dispatch ad
         WHERE ad.order_id = pl.order_id;
        ALTER TABLE order_assembly_dispatch DROP COLUMN actual_pump_status;
        ALTER TABLE order_assembly_dispatch DROP COLUMN assembled_packed_qty;
        ALTER TABLE order_assembly_dispatch DROP COLUMN assembly_date;
    END IF;
END $$;

