CREATE TABLE IF NOT EXISTS order_qc_requirement_documents (
    id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    order_id      UUID NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    file_name     TEXT NOT NULL,
