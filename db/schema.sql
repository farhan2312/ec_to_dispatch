-- ---------------------------------------------------------------------------
-- users
-- Roles and access status match the login / "Request Access" signup flow.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS users (
    id            UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    full_name     TEXT         NOT NULL,
    email         TEXT         NOT NULL,
    password_hash TEXT         NOT NULL,
    role          TEXT         NOT NULL
                               CHECK (role IN ('admin', 'central_visibility', 'operations',
                                               'accounts', 'drawing', 'planning', 'purchase',
                                               'qc', 'dispatch')),
    status        TEXT         NOT NULL DEFAULT 'pending'
                               CHECK (status IN ('pending', 'approved', 'rejected', 'disabled')),
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at    TIMESTAMPTZ  NOT NULL DEFAULT now()
);

-- Case-insensitive unique email (so Jane@x.com == jane@x.com).
CREATE UNIQUE INDEX IF NOT EXISTS users_email_lower_key
    ON users (lower(email));

-- Bring existing tables up to date with the CHECK constraints above.
-- Drop the old role check first, migrate the legacy 'assembly' role to the new
-- 'dispatch' role, then apply the expanded role set.
ALTER TABLE users DROP CONSTRAINT IF EXISTS users_role_check;
UPDATE users SET role = 'dispatch' WHERE role = 'assembly';
ALTER TABLE users ADD  CONSTRAINT users_role_check
    CHECK (role IN ('admin', 'central_visibility', 'operations', 'accounts',
                    'drawing', 'planning', 'purchase', 'qc', 'dispatch'));
ALTER TABLE users DROP CONSTRAINT IF EXISTS users_status_check;
ALTER TABLE users ADD  CONSTRAINT users_status_check
    CHECK (status IN ('pending', 'approved', 'rejected', 'disabled'));

-- Keep updated_at current on every UPDATE.
CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS users_set_updated_at ON users;
CREATE TRIGGER users_set_updated_at
    BEFORE UPDATE ON users
    FOR EACH ROW
    EXECUTE FUNCTION set_updated_at();

-- ---------------------------------------------------------------------------
-- audit_log — activity trail: who signed in, when, and what they did.
-- Actor identity is denormalized so entries survive user deletion and so
-- failed logins (no user row) can still be recorded.
-- ---------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS audit_log (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id     UUID REFERENCES users(id) ON DELETE SET NULL,
    user_email  TEXT,
    user_role   TEXT,
    action      TEXT NOT NULL,   -- e.g. login, order.create, access.approve
    category    TEXT NOT NULL,   -- auth | activity | ownership
    target      TEXT,            -- what was affected (SO/EC, user email, …)
    details     TEXT,            -- human-readable summary
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS audit_log_created_at_idx ON audit_log (created_at DESC);
CREATE INDEX IF NOT EXISTS audit_log_category_idx ON audit_log (category);
CREATE INDEX IF NOT EXISTS audit_log_user_idx ON audit_log (user_id);

-- ===========================================================================
-- Order-to-Dispatch master tracker (normalized)
--
-- A core `orders` row, one detail table per department (1:1 via order_id),
-- and child tables for pumps and lots (1:many). Every detail/child table
-- references orders(id) ON DELETE CASCADE. Column letters map to
-- assets/excelData.xlsx. Free-form/status fields are TEXT so messy imports
-- don't fail; clear dates/money/qty use proper types for sorting and totals.
-- ===========================================================================

-- Core identity — cols A–V (client/agent kept as plain text for now),
-- plus BH Project and BP Order Value.
CREATE TABLE IF NOT EXISTS orders (
    id                       UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    sl_no                    BIGINT GENERATED BY DEFAULT AS IDENTITY,  -- A  Sl. No.
    so_no                    TEXT,           -- B  SO NO.
    ec_no                    TEXT,           -- C  EC No.
    ec_generated_date        DATE,           -- D  EC Generated Date
    ec_rcvd_operations_date  DATE,           -- E  EC RCVD In OPERATIONS
    ec_sent_production_date  DATE,           -- F  EC sent date in Production
    file_no                  TEXT,           -- G  FILE NO.
    client_code              TEXT,           -- H  CLIENT CODE
    client_type              TEXT,           -- I  Client Type
    party                    TEXT,           -- J  Party
    nature_of_supply         TEXT,           -- K  Nature of Supply
    industry_type            TEXT,           -- L  INDUSTRY TYPE
    agent                    TEXT,           -- M  AGENT
    item                     TEXT,           -- N  Item
    po_no                    TEXT,           -- O  PO NO.
    customer_po_date         DATE,           -- P  Customer PO Date
    model_no                 TEXT,           -- Q  Model No.
    pump_qty                 INTEGER,        -- R  IF PUMP (QTY)
    pump_sno                 TEXT,           -- S  PUMP S.NO.
    orientation              TEXT,           -- T  ORIENTATION
    liquid_application       TEXT,           -- U  LIQUID/ APPLICATION
    version                  TEXT,           -- V  VERSION
    project                  TEXT,           -- BH Project
    payment_terms            TEXT,           -- W  Payment Terms (Central Visibility-owned)
    master_reason_of_delay   TEXT,           -- BK Master Reason of Delay (order-level)
    dispatch_target_date     DATE,           -- AU DISP. TARGET DT. (set after payment confirmation)
    dispatch_target_revised_date DATE,       -- AV Revise Disp. Target Dt (when LD)
    drg_target_date          DATE,           -- AG Target Dt. For Drg (Central Visibility-owned)
    order_value              NUMERIC(14,2),  -- BP Order Value
    created_at               TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at               TIMESTAMPTZ NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS orders_so_no_idx ON orders (so_no);
CREATE INDEX IF NOT EXISTS orders_ec_no_idx ON orders (ec_no);

-- Billing & Operations / PI — cols W–AB.
CREATE TABLE IF NOT EXISTS order_billing (
    order_id             UUID PRIMARY KEY REFERENCES orders(id) ON DELETE CASCADE,
    freight_terms        TEXT,           -- X  Freight Terms
    packing_requirement  TEXT,           -- Y  Packing Requirement
    pi_no                TEXT,           -- Z  PI No.
    pi_date              DATE,           -- AA PI Date
    pi_value             NUMERIC(14,2),  -- AB PI Value
    created_at           TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at           TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Accounts — col AC + the Payment Confirmed gate.
-- payment_status carries the gate value (Pending / Received / Confirmed / Hold);
-- 'Hold' means not confirmed → escalated to Central Visibility.
CREATE TABLE IF NOT EXISTS order_accounts (
    order_id                UUID PRIMARY KEY REFERENCES orders(id) ON DELETE CASCADE,
    payment_status          TEXT,        -- AC Payment Status (gate value)
    payment_confirmed_date  DATE,        -- set when payment is Confirmed
    hold_reason             TEXT,        -- escalation note when on Hold
    created_at              TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Add the gate columns to any pre-existing order_accounts table.
ALTER TABLE order_accounts ADD COLUMN IF NOT EXISTS payment_confirmed_date DATE;
ALTER TABLE order_accounts ADD COLUMN IF NOT EXISTS hold_reason TEXT;

-- Drawing — cols AD–AG ("in case of Pump").
